package net.datacrow.core.db;

import java.sql.Connection;

public abstract class Database {

    private final int index;
    
    public Database(int index)  {
        this.index = index;
    }
    
    public int getIndex() {
        return index;
    }
    
    public abstract String getName();
    
    public abstract void addQuery(Query query);
    
    public abstract void initiliaze(Connection connection) throws Exception;
}
