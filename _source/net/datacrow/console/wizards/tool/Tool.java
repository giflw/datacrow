package net.datacrow.console.wizards.tool;

public class Tool {
    
    private int module; 
    private String plugin;
    
    public Tool() {}

    public int getModule() {
        return module;
    }

    public void setModule(int module) {
        this.module = module;
    }

    public String getPlugin() {
        return plugin;
    }

    public void setPlugin(String plugin) {
        this.plugin = plugin;
    }
}
